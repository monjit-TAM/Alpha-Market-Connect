import { useQuery } from "@tanstack/react-query";
import { Navbar } from "@/components/navbar";
import { Card, CardContent } from "@/components/ui/card";
import { Badge } from "@/components/ui/badge";
import { Skeleton } from "@/components/ui/skeleton";
import { Avatar, AvatarFallback } from "@/components/ui/avatar";
import { BarChart3, Calendar } from "lucide-react";
import { Link } from "wouter";
import type { Content } from "@shared/schema";

type ContentWithAdvisor = Content & {
  advisor: { id: string; username: string; companyName: string | null; logoUrl: string | null };
};

export default function MarketOutlook() {
  const { data: items, isLoading } = useQuery<ContentWithAdvisor[]>({
    queryKey: ["/api/content/public/MarketUpdate"],
  });

  return (
    <div className="min-h-screen bg-background">
      <Navbar />
      <div className="max-w-5xl mx-auto px-4 md:px-6 py-8">
        <div className="space-y-1 mb-8">
          <div className="flex items-center gap-2">
            <BarChart3 className="w-6 h-6 text-primary" />
            <h1 className="text-2xl font-bold" data-testid="text-market-outlook-title">Market Outlook</h1>
          </div>
          <p className="text-muted-foreground">Market updates, commentaries and forecasts published by our advisors</p>
        </div>

        {isLoading ? (
          <div className="space-y-4">
            {[1, 2, 3, 4].map((i) => (
              <Skeleton key={i} className="h-28 w-full" />
            ))}
          </div>
        ) : !items || items.length === 0 ? (
          <Card>
            <CardContent className="p-12 text-center text-muted-foreground">
              No market outlook articles published yet. Check back soon for updates from our advisors.
            </CardContent>
          </Card>
        ) : (
          <div className="space-y-3">
            {items.map((item) => (
              <Card key={item.id} className="hover-elevate" data-testid={`content-card-${item.id}`}>
                <CardContent className="p-5">
                  <div className="flex flex-col sm:flex-row gap-4">
                    <div className="flex-1 min-w-0 space-y-2">
                      <h3 className="font-semibold text-base" data-testid={`text-content-title-${item.id}`}>{item.title}</h3>
                      {item.body && (
                        <p className="text-sm text-muted-foreground line-clamp-2">{item.body}</p>
                      )}
                      <div className="flex items-center gap-3 flex-wrap">
                        <Link href={`/advisors/${item.advisor.id}`} data-testid={`link-advisor-${item.id}`}>
                          <div className="flex items-center gap-1.5 cursor-pointer">
                            <Avatar className="w-5 h-5">
                              <AvatarFallback className="text-[10px] bg-primary text-primary-foreground">
                                {(item.advisor.companyName || item.advisor.username).slice(0, 2).toUpperCase()}
                              </AvatarFallback>
                            </Avatar>
                            <span className="text-xs font-medium" data-testid={`text-advisor-name-${item.id}`}>
                              {item.advisor.companyName || item.advisor.username}
                            </span>
                          </div>
                        </Link>
                        {item.createdAt && (
                          <div className="flex items-center gap-1 text-xs text-muted-foreground">
                            <Calendar className="w-3 h-3" />
                            {new Date(item.createdAt).toLocaleDateString("en-IN", {
                              day: "numeric",
                              month: "short",
                              year: "numeric",
                            })}
                          </div>
                        )}
                        <Badge variant="secondary" className="text-xs">Market Update</Badge>
                      </div>
                    </div>
                  </div>
                </CardContent>
              </Card>
            ))}
          </div>
        )}
      </div>
    </div>
  );
}
